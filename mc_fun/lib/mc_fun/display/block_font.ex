  @char_width 5
  # char height is 7 rows (used implicitly by the bitmap structure)

  @doc "Get the bitmap for a single character. Returns space bitmap for unknown chars."
  @spec get_char_bitmap(String.t()) :: [[0 | 1]]
  def get_char_bitmap(char) do
    Map.get(@chars, String.upcase(char), @chars[" "])
  end

  @doc """
  Render a text string to a list of block placements.

  Text renders on a vertical plane: X-axis for columns (east), Y-axis for rows (top-down).
  Origin is the top-left corner of the first character.

  Options:
  - `:material` — block type (default "white_concrete")
  - `:spacing` — columns between characters (default 1)
  - `:direction` — `:east` (X+) or `:north` (Z-) for horizontal layout (default `:east`)

  Returns a list of `%{pos: %{x:, y:, z:}, block: material}`.
  """
  @spec render(String.t(), map(), keyword()) :: [map()]
  def render(text, origin, opts \\ []) do
    material = Keyword.get(opts, :material, "white_concrete")
    spacing = Keyword.get(opts, :spacing, 1)
    direction = Keyword.get(opts, :direction, :east)

    text
    |> String.graphemes()
    |> Enum.with_index()
    |> Enum.flat_map(fn {char, char_index} ->
      bitmap = get_char_bitmap(char)
      char_offset = char_index * (@char_width + spacing)

      bitmap
      |> Enum.with_index()
      |> Enum.flat_map(fn {row, row_index} ->
        row
        |> Enum.with_index()
        |> Enum.filter(fn {val, _} -> val == 1 end)
        |> Enum.map(fn {_, col_index} ->
          # Y goes down from origin (row 0 = top)
          y_offset = -row_index
          h_offset = char_offset + col_index

          pos =
            case direction do
              :east ->
                %{
                  x: Map.get(origin, :x, 0) + h_offset,
                  y: Map.get(origin, :y, 0) + y_offset,
                  z: Map.get(origin, :z, 0)
                }

              :north ->
                %{
                  x: Map.get(origin, :x, 0),
                  y: Map.get(origin, :y, 0) + y_offset,
                  z: Map.get(origin, :z, 0) - h_offset
                }
            end

          %{pos: pos, block: material}
        end)
      end)
    end)
  end
